public class Continueexample {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
System.out.println("print number 1-10");
for(int i =1;i<=10; i++){
    if(i==5)
        continue;
    System.out.println(i+"\t");}
    System.out.println("skip printing 5");}}
    
    

